// https://www.chessprogramming.org/Simplified_Evaluation_Function#Piece-Square_Tables
function generateEvalBoard(array) {
  const values = "0123456789abcdef"
  let string = "";
  
  for (const value of array.reverse())
  {
    var index = (value / 5) + 8;
    index = Math.min(index, values.length - 1);
    index = Math.max(index, 0);
    string += values[index]
  }
  
  var combinedStr = "";
  for (let i = 0; i < 4; i++)
  {
    combinedStr += `0x${string.substring(i * 16, (i + 1) * 16)}, `;
  }

  return `new ulong[] { ${combinedStr}},`;
}

